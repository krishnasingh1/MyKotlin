package com.coderkprathore.myfirstkotlin

// Var and Val KeyWord

fun main() {

    println(" Hello Kotlin!")

    // How to create an Variable
    // Two type of variable in Kitlin
    // Mutable and Immutable Variable

    // Var Keyword
    var name = "Krishna"  // Mutable Variable in kotlin
    name = " Krishan"
    println("Hello $name")

    // Val Keyword
    val myName = "Rathore"  // immutable Variable in kotlin
    println("Hello Krishna $myName")

    /* mutable and immutable. An immutable variable is one whose value cannot be changed,
       also known as unchangeable or read-only variable. On the other hand the value of the
       mutable variable can be changed. */

}